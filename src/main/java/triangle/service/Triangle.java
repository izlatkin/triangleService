package triangle.service;

public class Triangle {
    private String id;
    private double firstSide;
    private double secondSide;
    private double thirdSide;

    public String getId() {
        return id;
    }
    public double getFirstSide() {
        return firstSide;
    }
    public double getSecondSide() {
        return secondSide;
    }
    public double getThirdSide() {
        return thirdSide;
    }
}
